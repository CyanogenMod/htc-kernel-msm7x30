void mdp4_mixer_gc_lut_setup(struct mdp_info *mdp, int mixer_num)
{
	unsigned long base;
	uint32_t data;
	char val;
	int i, off;

	if (mixer_num) 	/* mixer number, /dev/fb0, /dev/fb1 */
		base = MDP4_OVERLAYPROC1_BASE;/* 0x18000 */
	else
		base = MDP4_OVERLAYPROC0_BASE;/* 0x10000 */

	base += 0x4000;	/* GC_LUT offset */

	off = 0;
	for (i = 0; i < 4096; i++) {
		val = gc_lut[i];
		data = (val << 16 | val << 8 | val); /* R, B, and G are same */
		mdp_writel(mdp, data, base + off);
		off += 4;
	}
}

