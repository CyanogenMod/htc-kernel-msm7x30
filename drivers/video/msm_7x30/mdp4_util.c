void mdp4_sw_reset(struct mdp_info *mdp, ulong bits)
{
	bits &= 0x1f;	/* 5 bits */
	mdp_writel(mdp, bits, 0x001c);	/* MDP_SW_RESET */

	while (mdp_readl(mdp, 0x001c) & bits) /* self clear when complete */
		;
	PR_DISP_WARN("mdp4_sw_reset: 0x%x\n", (int)bits);
}

void mdp4_overlay_cfg(struct mdp_info *mdp, int overlayer, int blt_mode, int refresh, int direct_out)
{
	ulong bits = 0;

	if (blt_mode)
		bits |= (1 << 3);
	refresh &= 0x03;	/* 2 bites */
	bits |= (refresh << 1);
	direct_out &= 0x01;
	bits |= direct_out;

	if (overlayer == MDP4_MIXER0)
	  mdp_writel(mdp, bits, 0x10004); /* MDP_OVERLAY0_CFG */
	else
	  mdp_writel(mdp, bits, 0x18004); /* MDP_OVERLAY1_CFG */

	PR_DISP_WARN("mdp4_overlay_cfg: 0x%x\n", (int)mdp_readl(mdp, 0x10004));
}

void mdp4_display_intf_sel(struct mdp_info *mdp, int output, ulong intf)
{
	ulong bits, mask, data;

	bits = mdp_readl(mdp, 0x0038);	/* MDP_DISP_INTF_SEL */

	if (intf == DSI_VIDEO_INTF) {
		data = 0x40;	/* bit 6 */
		intf = MDDI_LCDC_INTF;
		if (output == SECONDARY_INTF_SEL) {
			PR_DISP_INFO("%s: Illegal INTF selected, output=%d \
				intf=%d\n", __func__, output, (int)intf);
		}
	} else if (intf == DSI_CMD_INTF) {
		data = 0x80;	/* bit 7 */
		intf = MDDI_INTF;
		if (output == EXTERNAL_INTF_SEL) {
			PR_DISP_INFO("%s: Illegal INTF selected, output=%d \
				intf=%d\n", __func__, output, (int)intf);
		}
	} else
		data = 0;

	mask = 0x03;	/* 2 bits */
	intf &= 0x03;	/* 2 bits */

	switch (output) {
	case EXTERNAL_INTF_SEL:
		intf <<= 4;
		mask <<= 4;
		break;
	case SECONDARY_INTF_SEL:
		intf &= 0x02;	/* only MDDI and EBI2 support */
		intf <<= 2;
		mask <<= 2;
		break;
	default:
		break;
	}

	intf |= data;
	mask |= data;

	bits &= ~mask;
	bits |= intf;

	mdp_writel(mdp, bits, 0x0038); /* MDP_DISP_INTF_SEL */

  PR_DISP_WARN("mdp4_display_intf_sel: 0x%x\n", (int)mdp_readl(mdp, 0x0038));
}

unsigned long mdp4_display_status(struct mdp_info *mdp)
{
	return mdp_readl(mdp, 0x0018) & 0x3ff;	/* MDP_DISPLAY_STATUS */
}

void mdp4_ebi2_lcd_setup(struct mdp_info *mdp, int lcd, ulong base, int ystride)
{
	/* always use memory map */
	ystride &= 0x01fff;	/* 13 bits */
	if (lcd == EBI2_LCD0) {
		mdp_writel(mdp, base, 0x0060);/* MDP_EBI2_LCD0 */
		mdp_writel(mdp, ystride, 0x0068);/* MDP_EBI2_LCD0_YSTRIDE */
	} else {
		mdp_writel(mdp, base, 0x0064);/* MDP_EBI2_LCD1 */
		mdp_writel(mdp, ystride, 0x006c);/* MDP_EBI2_LCD1_YSTRIDE */
	}
}

void mdp4_mddi_setup(struct mdp_info *mdp, int mddi, unsigned long id)
{
	ulong 	bits;

	if (mddi == MDDI_EXTERNAL_SET)
		bits = 0x02;
	else if (mddi == MDDI_SECONDARY_SET)
		bits = 0x01;
	else
		bits = 0;	/* PRIMARY_SET */

	id <<= 16;

	bits |= id;

  mdp_writel(mdp, bits, 0x0090);  /* MDP_MDDI_PARAM_WR_SEL */

}



void mdp4_fetch_cfg(struct mdp_info *mdp, uint32_t clk, uint32_t pclk)
{

	uint32_t dmap_data, vg_data;
	uint32_t base;
	int i;

	if (clk <= pclk)
		PR_DISP_INFO("mdp4_fetch_cfg: ERROR!! mdp_clk=%d \
				<= mdp_pclk=%d\n", (int)clk, (int)pclk);

	if (clk >= 90000000) { /* 90 Mhz */
		dmap_data = 0x47; /* 16 bytes-burst x 8 req */
		vg_data = 0xc7; /* 16 bytes-burs x 8 req */
	} else {
		dmap_data = 0x27; /* 8 bytes-burst x 8 req */
		vg_data = 0xc3; /* 16 bytes-burst x 4 req */
	}

	PR_DISP_INFO("mdp4_fetch_cfg: dmap=%x vg=%x\n",
			dmap_data, vg_data);

	/* dma_p fetch config */
	mdp_writel(mdp, dmap_data, 0x91004);
	/* dma_e fetch config */
	mdp_writel(mdp, dmap_data, 0xB1004);

	/*
	 * set up two vg pipes and two rgb pipes
	 */
	base = MDP4_VIDEO_BASE;
	for (i = 0; i < 4; i++) {
		mdp_writel(mdp, vg_data, base + 0x1004);
		base += MDP4_VIDEO_OFF;
	}
}

void mdp4_hw_init(struct mdp_info *mdp)
{
	ulong bits;

	PR_DISP_WARN("%s\n", __func__);
#ifdef MDP4_ERROR
	/*
	 * Issue software reset on DMA_P will casue DMA_P dma engine stall
	 * on LCDC mode. However DMA_P does not stall at MDDI mode.
	 * This need further investigation.
	 */
	mdp4_sw_reset(mdp, 0x17);
#endif

	mdp4_clear_lcdc(mdp);

	mdp4_mixer_blend_init(mdp, 0);
	mdp4_mixer_blend_init(mdp, 1);
	mdp4_vg_qseed_init(mdp, 0);
	mdp4_vg_qseed_init(mdp, 1);

	/* yuv2rgb */
	mdp4_vg_qseed_init(mdp, 0);
	mdp4_vg_qseed_init(mdp, 1);
	mdp4_vg_csc_mv_setup(mdp, 0);
	mdp4_vg_csc_mv_setup(mdp, 1);
	mdp4_vg_csc_pre_bv_setup(mdp, 0);
	mdp4_vg_csc_pre_bv_setup(mdp, 1);
	mdp4_vg_csc_post_bv_setup(mdp, 0);
	mdp4_vg_csc_post_bv_setup(mdp, 1);
	mdp4_vg_csc_pre_lv_setup(mdp, 0);
	mdp4_vg_csc_pre_lv_setup(mdp, 1);
	mdp4_vg_csc_post_lv_setup(mdp, 0);
	mdp4_vg_csc_post_lv_setup(mdp, 1);

	/* rgb2yuv */
	mdp4_mixer1_csc_mv_setup(mdp);
	mdp4_mixer1_csc_pre_bv_setup(mdp);
	mdp4_mixer1_csc_post_bv_setup(mdp);
	mdp4_mixer1_csc_pre_lv_setup(mdp);
	mdp4_mixer1_csc_post_lv_setup(mdp);

	mdp4_mixer_gc_lut_setup(mdp, 0);
	mdp4_mixer_gc_lut_setup(mdp, 1);

	mdp4_vg_igc_lut_setup(mdp, 0);
	mdp4_vg_igc_lut_setup(mdp, 1);

	 mdp4_rgb_igc_lut_setup(mdp, 0);
	 mdp4_rgb_igc_lut_setup(mdp, 1);


	mdp_writel(mdp, 0x3, MDP_EBI2_PORTMAP_MODE);

	/* system interrupts */
	bits =  MDP4_ANY_INTR_MASK;
	mdp_writel(mdp, bits, 0x0050);/* enable specififed interrupts */

	/* histogram */
	mdp_writel(mdp, 1, 0x95010);	/* auto clear HIST */

	/* enable histogram interrupts */
	mdp_writel(mdp, INTR_HIST_DONE, 0x9501c);
#if defined(CONFIG_FB_MSM_MDP_ABL)
	mdp_writel(mdp, MDP_HIST_DONE, MDP_INTR_ENABLE);
#endif
	/* For the max read pending cmd config below, if the MDP clock     */
	/* is less than the AXI clock, then we must use 3 pending          */
	/* pending requests.  Otherwise, we should use 8 pending requests. */
	/* In the future we should do this detection automatically.	   */

	/* max read pending cmd config */
	mdp_writel(mdp, 0x02222, 0x004c);	/* 3 pending requests */

#ifndef CONFIG_FB_MSM_OVERLAY
	/* both REFRESH_MODE and DIRECT_OUT are ignored at BLT mode */
	mdp4_overlay_cfg(mdp, MDP4_MIXER0, OVERLAY_MODE_BLT, 0, 0);
	mdp4_overlay_cfg(mdp, MDP4_MIXER1, OVERLAY_MODE_BLT, 0, 0);
#endif

}


void mdp4_clear_lcdc(struct mdp_info *mdp)
{
	uint32_t bits;

	bits = mdp_readl(mdp, 0xc0000);
	if (bits & 0x01) /* enabled already */
		return;

	mdp_writel(mdp, 0, 0xc0004);	/* vsync ctrl out */
	mdp_writel(mdp, 0, 0xc0008);	/* vsync period */
	mdp_writel(mdp, 0, 0xc000c);	/* vsync pusle width */
	mdp_writel(mdp, 0, 0xc0010);	/* lcdc display HCTL */
	mdp_writel(mdp, 0, 0xc0014);	/* lcdc display v start */
	mdp_writel(mdp, 0, 0xc0018);	/* lcdc display v end */
	mdp_writel(mdp, 0, 0xc001c);	/* lcdc active hctl */
	mdp_writel(mdp, 0, 0xc0020);	/* lcdc active v start */
	mdp_writel(mdp, 0, 0xc0024);	/* lcdc active v end */
	mdp_writel(mdp, 0, 0xc0028);	/* lcdc board color */
	mdp_writel(mdp, 0, 0xc002c);	/* lcdc underflow ctrl */
	mdp_writel(mdp, 0, 0xc0030);	/* lcdc hsync skew */
	mdp_writel(mdp, 0, 0xc0034);	/* lcdc test ctl */
	mdp_writel(mdp, 0, 0xc0038);	/* lcdc ctl polarity */
}




