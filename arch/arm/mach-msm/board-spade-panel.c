static int lcm_auo_write_seq(struct lcm_cmd *cmd_table, unsigned size)
{
        int i;

        for (i = 0; i < size; i++) {
		if (cmd_table[i].cmd == LCM_MDELAY) {
			hr_msleep(cmd_table[i].data);
			continue;
		}
		qspi_send_16bit(cmd_table[i].cmd, cmd_table[i].data);
	}
        return 0;
}

